            Ed-Cademy Educator Policy (Subsidiary
            of Ed-Cred)
        </h1>

        <p>
            As part of the Ed-Cred ecosystem, Ed-Cademy ensures that educators operate within a
            framework that protects learners, upholds academic standards, and promotes ethical
            business practices.
        </p>

        <h2>1. Verification and Credentialing</h2>
        <p>
            Educators must submit proof of teaching credentials or tutoring qualifications/experience.
            Verified accounts will receive a 'Credential Verified' stamp for teachers and ‘Tutor Verified’
            stamp.
        </p>

        <h2>2. Content Standards</h2>
        <p>
            All materials uploaded must be academic in nature. Non-academic content will be removed.
            Content is subject to random quality reviews.
        </p>

        <h2>3. Intellectual Property</h2>
        <p>
            All submitted content must be original or legally licensed. AI tools may assist in lesson
            planning, but final outputs must reflect educator input.
        </p>

        <h2>4. Watermarking</h2>
        <p>
            Materials are watermarked with Ed-Cademy's branding. Educators may opt to include their
            name on content for a 2% commission reduction.
        </p>

        <h2>5. Session Delivery</h2>
        <p>
            All tutoring must be conducted via Ed-Cademy's video platform. Sessions are recorded,
            accessible to participants for one month, and retained indefinitely for training and
            compliance.
        </p>

        <h2>6. Third-Party Restriction</h2>
        <p>
            Outsourcing to unverified tutors or redirecting learners off-platform is prohibited.
        </p>

        <h2>7. Payments and Commission</h2>
        <ul>
            <li>Basic Plan: 70% educator payout.</li>
            <li>Premium Plan: 85% payout for $59/year</li>
            <li>Earnings may be withheld for violations.</li>
        </ul>


        <h2>8. Review and Moderation</h2>
        <p>
            Content and conduct are subject to moderation. Violations may lead to takedown or
            removal.
        </p>

        <h2>9. Code of Conduct</h2>
        <p>
            Educators must act professionally and ethically. Misuse, harassment, or dishonesty will
            result in suspension or termination.
        </p>
        <p>
            Only individuals who are 18 years or older may register and operate as educators on Ed-
            Cademy. </p>
        <p>
            Accounts found to be in violation of this age requirement will be removed.
        </p>

