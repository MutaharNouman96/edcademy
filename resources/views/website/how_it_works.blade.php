    @push('scripts')
    <script>
        // Scroll-triggered reveal for .hiw-anim elements
        (function() {
            const els = document.querySelectorAll('.hiw-anim');
            if (!els.length) return;

            const io = new IntersectionObserver(function(entries) {
                entries.forEach(function(entry, i) {
                    if (entry.isIntersecting) {
                        // Stagger siblings
                        const parent = entry.target.closest('.row');
                        if (parent) {
                            const siblings = parent.querySelectorAll('.hiw-anim');
                            siblings.forEach(function(el, idx) {
                                setTimeout(function() { el.classList.add('hiw-visible'); }, idx * 120);
                            });
                        } else {
                            entry.target.classList.add('hiw-visible');
                        }
                        io.unobserve(entry.target);
                    }
                });
            }, { threshold: 0.15, rootMargin: '0px 0px -60px 0px' });

            els.forEach(function(el) { io.observe(el); });
        })();
    </script>
